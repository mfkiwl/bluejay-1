//==============================================
// Jay::lb
//==============================================
uint64_t Jay::lb(uint64_t rs1)
{
    uint64_t rd;
    asm volatile ("lb %[rd], 0(%[rs1])" : [rd] "=r" (rd) : [rs1] "r" (rs1) : );
    return rd;
}

